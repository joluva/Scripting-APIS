# API-2 MOD-2. Realizar operaciones numéricas
# Sumar dentro de una variable y mostrar salida en formato float
add_number=float(10 + 3)
print("-------------------------------------------------------------------\n")
print(f"La suma de los valores 10 + 3 da como resultado float: {add_number}\n")
print("-------------------------------------------------------------------\n")

# Calcular el cociente de 10 / 3 y mostrarlo como entero
div_number=int(10 / 3)
print("-------------------------------------------------------------------\n")
print(f"La cociente entre 10 / 3 da como resultado int: {div_number}\n")
print("-------------------------------------------------------------------\n")

# Realizar el producto entre los números 5 * 2 y mostrarlo como float
mult_number=float(5 * 2)
print("-------------------------------------------------------------------\n")
print(f"El producto entre 5 * 2 da como resultado float: {mult_number}\n")
print("-------------------------------------------------------------------\n")